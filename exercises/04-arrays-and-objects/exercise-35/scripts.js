let calculator = {
	sum: (a, b) => a + b,

	subtraction: (a, b) => a - b,

	division: (a, b) => a / b,

	multiplication: (a, b) => a * b
}

console.log(calculator.sum(2, 5));
console.log(calculator.subtraction(6, 3));
console.log(calculator.division(8, 5));
console.log(calculator.multiplication(1.25, 5));